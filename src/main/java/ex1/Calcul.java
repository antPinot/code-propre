package ex1;

/**
 * @author antPinot
 * 
 * Cette classe permet de réaliser un calcul de base : 
 * une addition ou une soustraction
 *
 */
public class Calcul {

	/**
	 * Renvoie la somme de a et b
	 * @param a : entier 1
	 * @param b : entier 2
	 * @return int
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Renvoie la différence de a et b
	 * @param a entier 1
	 * @param b entier 2
	 * @return int
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
